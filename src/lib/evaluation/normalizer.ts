import { ResumeEvaluationSchema } from "@/lib/evaluation/schema";
import type {
  DimensionScore,
  Evidence,
  EvaluationConfidenceLevel,
  EvaluationEvidenceRelevance,
  EvaluationEvidenceSource,
  EvaluationInterviewQuestionCategory,
  EvaluationRecommendation,
  EvaluationRiskType,
  EvaluationSeverity,
  InterviewQuestion,
  ResumeEvaluationResult,
  Risk,
  Strength,
  Weakness
} from "@/types/evaluation-output";

type NormalizeResult =
  | {
      success: true;
      data: ResumeEvaluationResult;
    }
  | {
      success: false;
      error: string;
    };

const recommendationMap: Record<string, EvaluationRecommendation> = {
  good_fit: "STRONG_FIT",
  high_fit: "STRONG_FIT",
  maybe: "UNCERTAIN",
  needs_review: "NOT_ENOUGH_EVIDENCE",
  no_evidence: "NOT_ENOUGH_EVIDENCE",
  not_enough_info: "NOT_ENOUGH_EVIDENCE",
  potential: "POTENTIAL_FIT",
  potential_fit: "POTENTIAL_FIT",
  review_needed: "NOT_ENOUGH_EVIDENCE",
  strong: "STRONG_FIT",
  strong_fit: "STRONG_FIT",
  uncertain: "UNCERTAIN"
};

export function normalizeResumeEvaluationResult(input: unknown): NormalizeResult {
  if (!isRecord(input)) {
    return { success: false, error: "Evaluation output must be an object." };
  }

  const normalized: ResumeEvaluationResult = {
    schemaVersion: "m07-b3-a.v1",
    recommendation:
      normalizeRecommendation(input.recommendation) ?? "NOT_ENOUGH_EVIDENCE",
    confidence: normalizeConfidence(input.confidence) ?? "LOW",
    overallScore: normalizeScore(input.overallScore ?? input.score ?? 0),
    overallSummary:
      normalizeText(input.overallSummary ?? input.summary) ??
      "No evaluation summary provided.",
    dimensionScores: normalizeDimensionScores(input.dimensionScores),
    evidence: normalizeEvidence(input.evidence),
    strengths: normalizeStrengths(input.strengths),
    weaknesses: normalizeWeaknesses(input.weaknesses),
    risks: normalizeRisks(input.risks),
    interviewQuestions: normalizeInterviewQuestions(input.interviewQuestions),
    notes: normalizeNullableText(input.notes)
  };

  if (!normalizeRecommendation(input.recommendation) && input.recommendation !== undefined) {
    return { success: false, error: "Unsupported recommendation value." };
  }

  const result = ResumeEvaluationSchema.safeParse(normalized);

  if (!result.success) {
    return {
      success: false,
      error: result.error.issues[0]?.message ?? "Evaluation output does not match schema."
    };
  }

  return { success: true, data: result.data };
}

function normalizeDimensionScores(value: unknown): DimensionScore[] {
  if (!Array.isArray(value)) {
    return [
      {
        evidenceIds: ["ev_default"],
        key: "overall",
        label: "Overall",
        rationale: "Default dimension score generated by the rule-based normalizer.",
        score: 0
      }
    ];
  }

  return value.filter(isRecord).map((item, index) => ({
    evidenceIds: normalizeEvidenceIds(item.evidenceIds),
    key: normalizeSlug(item.key) ?? `dimension-${index + 1}`,
    label: normalizeText(item.label) ?? `Dimension ${index + 1}`,
    rationale: normalizeText(item.rationale) ?? "No rationale provided.",
    score: normalizeScore(item.score)
  }));
}

function normalizeEvidence(value: unknown): Evidence[] {
  if (!Array.isArray(value)) {
    return [
      {
        id: "ev_default",
        relevance: "LOW",
        source: "RESUME",
        text: "No evidence provided."
      }
    ];
  }

  return value.filter(isRecord).map((item, index) => ({
    id: normalizeEvidenceId(item.id) ?? `ev_${index + 1}`,
    relevance: normalizeEvidenceRelevance(item.relevance) ?? "MEDIUM",
    source: normalizeEvidenceSource(item.source) ?? "RESUME",
    text: normalizeText(item.text ?? item.quote) ?? "No evidence text provided."
  }));
}

function normalizeStrengths(value: unknown): Strength[] {
  if (!Array.isArray(value)) {
    return [];
  }

  return value.filter(isRecord).map((item) => ({
    description: normalizeText(item.description ?? item.text) ?? "No description provided.",
    evidenceIds: normalizeEvidenceIds(item.evidenceIds),
    title: normalizeText(item.title) ?? "Strength"
  }));
}

function normalizeWeaknesses(value: unknown): Weakness[] {
  if (!Array.isArray(value)) {
    return [];
  }

  return value.filter(isRecord).map((item) => ({
    description: normalizeText(item.description ?? item.text) ?? "No description provided.",
    evidenceIds: normalizeEvidenceIds(item.evidenceIds),
    severity: normalizeSeverity(item.severity) ?? "MEDIUM",
    title: normalizeText(item.title) ?? "Weakness"
  }));
}

function normalizeRisks(value: unknown): Risk[] {
  if (!Array.isArray(value)) {
    return [];
  }

  return value.filter(isRecord).map((item) => ({
    description: normalizeText(item.description ?? item.text) ?? "No description provided.",
    evidenceIds: normalizeEvidenceIds(item.evidenceIds),
    severity: normalizeSeverity(item.severity) ?? "MEDIUM",
    type: normalizeRiskType(item.type) ?? "OTHER"
  }));
}

function normalizeInterviewQuestions(value: unknown): InterviewQuestion[] {
  if (!Array.isArray(value)) {
    return [
      {
        category: "OTHER",
        evidenceIds: ["ev_default"],
        purpose: "Clarify missing evaluation evidence.",
        question: "Can you share more detail about the experience most relevant to this role?"
      }
    ];
  }

  return value.filter(isRecord).map((item) => ({
    category: normalizeQuestionCategory(item.category) ?? "OTHER",
    evidenceIds: normalizeEvidenceIds(item.evidenceIds),
    purpose: normalizeText(item.purpose) ?? "Clarify relevant experience.",
    question: normalizeText(item.question) ?? "Can you share more detail?"
  }));
}

function normalizeScore(value: unknown): number {
  if (typeof value !== "number" || !Number.isFinite(value)) {
    return 0;
  }

  return Math.min(100, Math.max(0, Math.round(value)));
}

function normalizeRecommendation(value: unknown): EvaluationRecommendation | null {
  if (typeof value !== "string") {
    return null;
  }

  const normalized = normalizeEnumText(value);

  if (
    normalized === "STRONG_FIT" ||
    normalized === "POTENTIAL_FIT" ||
    normalized === "UNCERTAIN" ||
    normalized === "NOT_ENOUGH_EVIDENCE"
  ) {
    return normalized;
  }

  return recommendationMap[normalized.toLowerCase()] ?? null;
}

function normalizeConfidence(value: unknown): EvaluationConfidenceLevel | null {
  const normalized = normalizeEnumText(value);

  if (normalized === "LOW" || normalized === "MEDIUM" || normalized === "HIGH") {
    return normalized;
  }

  return null;
}

function normalizeEvidenceSource(value: unknown): EvaluationEvidenceSource | null {
  const normalized = normalizeEnumText(value);

  if (
    normalized === "RESUME" ||
    normalized === "JOB_PROFILE" ||
    normalized === "EVALUATION_CRITERIA"
  ) {
    return normalized;
  }

  return null;
}

function normalizeEvidenceRelevance(value: unknown): EvaluationEvidenceRelevance | null {
  const normalized = normalizeEnumText(value);

  if (normalized === "LOW" || normalized === "MEDIUM" || normalized === "HIGH") {
    return normalized;
  }

  return null;
}

function normalizeSeverity(value: unknown): EvaluationSeverity | null {
  const normalized = normalizeEnumText(value);

  if (normalized === "LOW" || normalized === "MEDIUM" || normalized === "HIGH") {
    return normalized;
  }

  return null;
}

function normalizeRiskType(value: unknown): EvaluationRiskType | null {
  const normalized = normalizeEnumText(value);

  if (
    normalized === "MISSING_REQUIREMENT" ||
    normalized === "INCONSISTENT_EVIDENCE" ||
    normalized === "ROLE_MISMATCH" ||
    normalized === "SENIORITY_MISMATCH" ||
    normalized === "OTHER"
  ) {
    return normalized;
  }

  return null;
}

function normalizeQuestionCategory(
  value: unknown
): EvaluationInterviewQuestionCategory | null {
  const normalized = normalizeEnumText(value);

  if (
    normalized === "TECHNICAL" ||
    normalized === "EXPERIENCE" ||
    normalized === "MOTIVATION" ||
    normalized === "RISK_FOLLOW_UP" ||
    normalized === "OTHER"
  ) {
    return normalized;
  }

  return null;
}

function normalizeEvidenceIds(value: unknown): string[] {
  if (!Array.isArray(value)) {
    return ["ev_default"];
  }

  const normalized = value
    .map(normalizeEvidenceId)
    .filter((item): item is string => item !== null);

  return normalized.length > 0 ? normalized : ["ev_default"];
}

function normalizeEvidenceId(value: unknown): string | null {
  if (typeof value !== "string") {
    return null;
  }

  const normalized = value.trim().replaceAll(/\s+/g, "_");

  if (!/^[a-zA-Z0-9_-]+$/.test(normalized)) {
    return null;
  }

  return normalized || null;
}

function normalizeSlug(value: unknown): string | null {
  if (typeof value !== "string") {
    return null;
  }

  const normalized = value.trim().toLowerCase().replaceAll(/[^a-z0-9]+/g, "-").replaceAll(/^-|-$/g, "");

  if (!/^[a-z][a-z0-9]*(?:-[a-z0-9]+)*$/.test(normalized)) {
    return null;
  }

  return normalized;
}

function normalizeNullableText(value: unknown): string | null {
  if (value === null || value === undefined) {
    return null;
  }

  return normalizeText(value);
}

function normalizeText(value: unknown): string | null {
  if (typeof value !== "string") {
    return null;
  }

  const normalized = value.trim();

  return normalized || null;
}

function normalizeEnumText(value: unknown): string {
  if (typeof value !== "string") {
    return "";
  }

  return value.trim().replaceAll(/[\s-]+/g, "_").toUpperCase();
}

function isRecord(value: unknown): value is Record<string, unknown> {
  return typeof value === "object" && value !== null && !Array.isArray(value);
}
